const getNumber = (s) => {
  return s
    .split("")
    .reverse()
    .reduce((acc, n, i) => {
      acc += n * Math.pow(10, i);
      return acc;
    }, 0);
};

const sumLargeNumbers = (numbersArray) => {
  let sum = 0;
  for (let s of numbersArray) {
    sum += getNumber(s);
  }
  return sum;
};

